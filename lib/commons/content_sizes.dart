import 'package:flutter/material.dart';

class ContentSizes {
  static double height(BuildContext context) {
    return MediaQuery.of(context).size.height;
  }

  static double width(BuildContext context) {
    return MediaQuery.of(context).size.width;
  }

  static double dp4(BuildContext context) {
    return MediaQuery.of(context).size.width / 100;
  }

  static double dp6(BuildContext context) {
    return MediaQuery.of(context).size.width / 70;
  }

  static double dp8(BuildContext context) {
    return MediaQuery.of(context).size.width / 54;
  }

  static double dp10(BuildContext context) {
    return MediaQuery.of(context).size.width / 41;
  }

  static double dp12(BuildContext context) {
    return MediaQuery.of(context).size.width / 34;
  }

  static double dp13(BuildContext context) {
    return MediaQuery.of(context).size.width / 31;
  }

  static double dp14(BuildContext context) {
    return MediaQuery.of(context).size.width / 29;
  }

  static double dp16(BuildContext context) {
    return MediaQuery.of(context).size.width / 26;
  }

  static double dp18(BuildContext context) {
    return MediaQuery.of(context).size.width / 23;
  }

  static double dp20(BuildContext context) {
    return MediaQuery.of(context).size.width / 20;
  }

  static double dp22(BuildContext context) {
    return MediaQuery.of(context).size.width / 17;
  }

  static double dp24(BuildContext context) {
    return MediaQuery.of(context).size.width / 16;
  }

  static double dp25(BuildContext context) {
    return MediaQuery.of(context).size.width / 14;
  }

  static double dp28(BuildContext context) {
    return MediaQuery.of(context).size.width / 13;
  }

  static double dp30(BuildContext context) {
    return MediaQuery.of(context).size.width / 12;
  }

  static double dp36(BuildContext context) {
    return MediaQuery.of(context).size.width / 9.7;
  }
}
